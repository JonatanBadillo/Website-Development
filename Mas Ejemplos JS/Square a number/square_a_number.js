// This function takes a number and returns the square of that number.
var square_a_number = function(num) {
	var value = num * num;
	return value;
};
// Call the function square_a_number
alert("The square of 3 is " + square_a_number(3));
// Call the function square_a_number
alert("The square of 4 is " + square_a_number(4));
